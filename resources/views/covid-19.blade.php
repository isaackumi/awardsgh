<h6>Announcing the Coronavirus Reporting Award 2020  </h6>
<p>
  National Communications Awards this year come at an unusual time, as we face the Covid-19 pandemic.
In response to the exceptional work of journalists and filmmakers covering the impact of the COVID-19 pandemic in Ghana, we are launching the Coronavirus Reporting Award 2020, supported by MoC.
We did not want to wait till next year. Entries are open now and the winner will be announced in November 2020.
This is a national story like few others. We’re connected by a shared crisis that presents us all with new ways of living and seeing the world.
We want to champion stories that show us what is happening beyond our borders and spotlight international perspectives on the pandemic.
We’re looking for stories that show us the potential solutions, the urgent needs and the surprising implications of COVID-19 around the world.
The Award will be for a piece of media in any format, including print, film, audio, or digital media. You can find the eligibility and selection criteria below.
The new Award is open for entries now. Deadline 30th August 2020.


</p>

<h6>Eligibility</h6>

<p>
  All entries should focus on stories, topics or issues in, about or related to Ghana, The awarding board will be looking for media that covers underreported stories or new angles and approaches to more familiar stories.
Entries must have had their first screening, broadcast, theatrical release, launch or publication between March 2020 and 30 July 2020.
The Coronavirus Reporting Award is open to entrants across Ghana, as long as the work meets one of the following criteria: <br>

•	the entry has been broadcast, screened or published (in print or in an established online publication/website) in the Ghana; OR

</p>

<h6>Selection Criteria</h6>

<p>Entries will be evaluated based on the overall merit of the piece, with an emphasis on:</p>

<h6>Relevance of the coverage</h6>

<p>

  •	The focus must be on stories or topics in, about or related to Ghana<br>
  •	Include the voices and views of people in Ghana

</p>


<h6>Quality of the coverage</h6>

<p>
  •	Well-crafted, structured and engaging<br>
•	High production and editorial values<br>
•	Distils and presents complex topics to the target audience in an understandable and accessible way

</p>

<h6>Originality and creativity of the coverage</h6>
<p>
  •	All entries should highlight underreported stories or issues, or explore new angles on familiar topics.
•	We are particularly looking at how the story is told: Is it amplifying voices that are normally unheard? Is it an inspiring story that will create change? Does the story challenge misperceptions or stereotypes in Ghana?

</p>


<h6>Substance and accuracy of the coverage</h6>

<p>
  •	Depth of research and analysis<br>
•	Well-researched, accurate and balanced reporting

</p>

<h6>Impact and reach of the coverage</h6>

<p>
  •	Demonstrates the actual impact or potential for the coverage to catalyse change<br>
•	May have prompted additional media coverage or policy change<br>
•	Consideration may be given to the total audience reached

</p>

<h6>Media Personality at the frontline </h6>

<p>
  This category seeks to appreciate media persons who made it an effort to go down COVID-19 centres to report reality to citizens and also give information that helped citizens to understand the virus and how to keep safe.
</p>










